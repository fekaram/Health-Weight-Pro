// Health Weight Pro targets Brazilian users primarily: unset/unknown language values
// default to Portuguese (Brazil); English remains fully supported when explicitly chosen.
export function normalizeLanguage(language) {
  return language === 'en' ? 'en' : 'pt-BR';
}

export function normalizeTheme(theme) {
  return ['light', 'dark', 'system'].includes(theme) ? theme : 'system';
}

export function getUnitsForLanguage(language) {
  return UNITS_BY_LANGUAGE[normalizeLanguage(language)];
}

export function createI18nContext(settings = {}) {
  const language = normalizeLanguage(settings.language);
  const units = getUnitsForLanguage(language);

  return {
    language,
    units,
    t(key, params = {}) {
      return translate(language, key, params);
    },
    formatDate(value) {
      return formatDateForLanguage(value, language);
    },
    formatLongDate(value) {
      return formatLongDateForLanguage(value, language);
    },
    formatNumber(value, options = {}) {
      return formatNumberForLanguage(value, language, options);
    },
  };
}

export function translate(language, key, params = {}) {
  const dictionary = translations[normalizeLanguage(language)] ?? translations.en;
  const template = dictionary[key] ?? translations.en[key] ?? key;

  return Object.entries(params).reduce(
    (text, [paramKey, paramValue]) => text.replaceAll(`{${paramKey}}`, String(paramValue ?? '')),
    template,
  );
}

export function formatDateForLanguage(value, language) {
  if (!value) {
    return '';
  }

  const [year, month, day] = String(value).slice(0, 10).split('-');

  if (!year || !month || !day) {
    return String(value);
  }

  return normalizeLanguage(language) === 'pt-BR' ? `${day}/${month}/${year}` : `${month}/${day}/${year}`;
}

export function formatLongDateForLanguage(value, language) {
  if (!value) {
    return '';
  }

  const date = parseLocalDate(value);

  if (Number.isNaN(date.getTime())) {
    return String(value);
  }

  return new Intl.DateTimeFormat(normalizeLanguage(language), {
    weekday: 'long',
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  }).format(date);
}

export function formatNumberForLanguage(value, language, options = {}) {
  const numberValue = Number(value);

  if (!Number.isFinite(numberValue)) {
    return '';
  }

  return new Intl.NumberFormat(normalizeLanguage(language), {
    maximumFractionDigits: options.maximumFractionDigits ?? 1,
    minimumFractionDigits: options.minimumFractionDigits ?? 0,
  }).format(numberValue);
}

export function applyDocumentPreferences({ language, theme } = {}) {
  const normalizedLanguage = normalizeLanguage(language);
  const normalizedTheme = normalizeTheme(theme);

  document.documentElement.lang = normalizedLanguage;
  document.documentElement.dataset.theme = normalizedTheme;
  document.documentElement.style.colorScheme =
    normalizedTheme === 'system' ? 'light dark' : normalizedTheme;
}
